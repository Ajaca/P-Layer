@Composable
fun FetchContent() {
    val data = listOf("Apple", "Banana", "Orange", "Grape")
    Box(
        modifier = Modifier
            .fillMaxSize()
            .padding(16.dp),
        contentAlignment = Alignment.Center
    ){
        SimpleList(data)
    }
}

@Composable
fun SimpleList(items: List<String>) {
    LazyColumn(verticalArrangement = Arrangement.spacedBy(1.dp)) {
        items(items) { item ->
            Text(
                text = item,
                modifier = Modifier
                    .fillMaxWidth()
                    .padding(16.dp)
            )
        }
    }
}


